#include <unistd.h>

void	rush(int x, int y)
{
	int	i;
	int	j;

	i = 0;
	while (i <= x)
	{
		j = 0;
		while(j <= y)
		{
			if ((j == 0 || j == y) && (i == 0 || i == x))
			{
				write(1, "o", 1);
			}
			else if ((j != 0 || j != y) && (i == 0 || i == x))
			{
				write(1, "-", 1);
			}
			else if ((j == 0 || j == y) && (i != 0 || i != x))
				write(1, "|", 1);
			else
				write(1, " ", 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}

int	main(void)
{
	rush(4,4);
}
